#include "../../includes/cube3d.h"

int			color_to_int(t_color c)
{
	return ((((c.r > 0xff) ? 0xff : c.r) << 16) |
			(((c.g > 0xff) ? 0xff : c.g) << 8) |
			((c.b > 0xff) ? 0xff : c.b));
}

t_color		int_to_color(int n)
{
	t_color	a;

	a.r = (n & 0x00FF0000) >> 16;
	a.g = (n & 0x0000FF00) >> 8;
	a.b = (n & 0x000000FF);
	return (a);
}